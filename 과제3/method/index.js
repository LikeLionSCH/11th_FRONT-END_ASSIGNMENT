// str.length() : 문자열의 길이 반환
const str1 = "Hello";
console.log(str1.length);

// str.toLowerCase() : 문자열을 소문자로 변환
const str2 = "Hello";
console.log(str2.toLowerCase()); 

// str.toUpperCase() : 문자열을 대문자로 변환
const str3 = "Hello";
console.log(str3.toUpperCase()); 

// str.split(): 문자열을 주어진 구분자를 기준으로 나누어 배열로 반환
const str4 = "Hello, World";
const arr = str4.split(", ");
console.log(arr); 

// parseInt(): 문자열을 정수로 변환
const num = parseInt("123");
console.log(num); 

// max(): 주어진 숫자 중에서 가장 큰 값을 반환
console.log(Math.max(1, 3, 10)); 

// min(): 주어진 숫자 중에서 가장 작은 값을 반환
console.log(Math.min(1, 5, 3));

// floor(): 주어진 숫자보다 작거나 같은 가장 큰 정수를 반환
console.log(Math.floor(4.9)); 

// round(): 주어진 숫자를 반올림하여 가장 가까운 정수를 반환
console.log(Math.round(3.1)); 
console.log(Math.round(3.9)); 

// random(): 0 이상 1 미만의 난수를 반환
console.log(Math.random()); 


// sqrt(): 주어진 숫자의 제곱근을 반환
console.log(Math.sqrt(25));








